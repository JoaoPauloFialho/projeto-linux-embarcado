static void ssd1306_command(uint8_t cmd) {
    uint8_t buffer[2] = {0x00, cmd};
    write(file_i2c, buffer, 2);
}

static void ssd1306_data(uint8_t data) {
    uint8_t buffer[2] = {0x40, data};
    write(file_i2c, buffer, 2);
}

void ssd1306_set_cursor(uint8_t col, uint8_t page) {
    ssd1306_command(0xB0 + page);          
    ssd1306_command(0x00 | (col & 0x0F));  
    ssd1306_command(0x10 | ((col >> 4) & 0x0F)); 
}

void ssd1306_clear(void) {
    for (uint8_t page = 0; page < 8; page++) {
        ssd1306_set_cursor(0, page);
        for (uint8_t col = 0; col < 128; col++) {
            ssd1306_data(0x00);
        }
    }
}

int init_oled(const char *i2c_bus, int i2c_addr) {
    if ((file_i2c = open(i2c_bus, O_RDWR)) < 0) return -1;
    if (ioctl(file_i2c, I2C_SLAVE, i2c_addr) < 0) return -1;

    ssd1306_command(0xAE); // Display OFF
    ssd1306_command(0xD5); ssd1306_command(0x80); 
    ssd1306_command(0xA8); ssd1306_command(0x3F); 
    ssd1306_command(0xD3); ssd1306_command(0x00); 
    ssd1306_command(0x40); 
    ssd1306_command(0x8D); ssd1306_command(0x14); 
    ssd1306_command(0x20); ssd1306_command(0x00); 
    ssd1306_command(0xA1); 
    ssd1306_command(0xC8); 
    ssd1306_command(0xDA); ssd1306_command(0x12); 
    ssd1306_command(0x81); ssd1306_command(0xCF); 
    ssd1306_command(0xD9); ssd1306_command(0xF1); 
    ssd1306_command(0xDB); ssd1306_command(0x40); 
    ssd1306_command(0xA4); 
    ssd1306_command(0xA6); 
    ssd1306_command(0xAF); // Display ON

    ssd1306_clear();
    return 0;
}

void oled_print(const char *str) {
    if (file_i2c < 0) return;
    while (*str) {
        char c = *str++;
        if (c >= ' ' && c <= 'z') {
            int index = c - ' ';
            for (int i = 0; i < 5; i++) {
                ssd1306_data(font5x8[index][i]); 
            }
            ssd1306_data(0x00); 
        } else {
            for(int i=0; i<6; i++) ssd1306_data(0x00);
        }
    }
    for(int i=0; i<16; i++) ssd1306_data(0x00);
}

void close_oled(void) {
    if (file_i2c >= 0) {
        ssd1306_clear();
        close(file_i2c);
        file_i2c = -1;
    }
}